    <p class="content"><strong>(ሰ)</strong> እዚ ገፅ ንኩሉ ክፍቲ ዝኮኑ ዝተፈላለዩ ናይ ርክብ ኣግባባት ማለት ውን ርኢቶ ዝወሃበሎም ብሎጋት፣ ትጥቅዑሎም ብሎጋት፣ ናይ ሓባር ቀጥታዊ መዋግዒ መድረካት፣ ፎረማት፣ መልእክቲ ዝጥቅዑሎም ሰሌዳታት፣ ናይ ዜና ጉጅለታት፣ ምህርቲ መለክዒታትን ጥቅሉል ሓበሬታታትን ናይ ማሕበራዊ ሚድያ ኣገልግሎታትን ወ.ዘ.ተ ክፍቲ ይገብር፡፡ ኣብ ልዕሊ ዞም መራከብቲ ኣግባባት እዚኦ ዝጥቀሙ ተገልገልቲ ብሓፈሻ ዝልጥፍዎም ንብረታት ትሕዝቶ መሰረት ብምግባር ዝኮነ ዓይነት ናይ ቅድመ ምፅራይን ክትትልን ተግባራት ዘይንገብር እንትንኮን እዚ ማለት ድማ ዝሓረይዎም ጥሪታት ብመንገዲ እዞም መራከብቲ ኣብዚ ገፅ ብምጣልፎም እዞም መሳርታት ብሓላፍነትን ስነ ምግባር ብዝተመልኦን መንገዲ ኣብ ጥቅሚ ናይ ምውዓል ሓላፍነት ከም ዘለዎም ንምግላፅ ይከውን፡፡ ከምቲ ቅድም ክብል ዝተገልፀ በዞም ክፍቲ ናይ ሓበሬታ መለዋወጢ ኣግባባት ኣቢሎም ንዝጠቅዕዎም ፣ ዝካፈልዎም ፣ ዝፅዕንዎም፣ ወይ ውን ዝብርግሕዎም ትሕዝቶታት ሓበሬታ ኩሎም i. ካብ ዘይሕጋዊ፣ መፈራራሕቲ፣ መፀለምታታት፣ ዓመፅ ዘለዎም፣ በዝበዝቲ፣ መተሓሓትቲ፣ መተዓናቀፍቲ፣ መታለልቲ፣ ገበቲ፣ ዘርኣዊ፣ ርኢቶ ሓዘል፣ ዘይግቡኣት ኢሉ ውን ካብ ዘይግቡእ ኣጠቃቅማ ቋንቋ ነፃ ክኮኑ ኣብ ምግባር ክሰማምዑ ይግባእ፡፡ ii. ኣብ ልዕሊ ናይ ንግዲ ምልክት፣ ፈጠራ በዓል ቤትነት፣ ናይ ንግዲ ምስጢርን ካልኦት መሰል ዋንነትን ናይ ዝኮነ ኣካል ዝግበር ጥሕሰት፡፡ iii. ዘይስልጣንካ ምጥቃምን ምድላልን ዓይነት ትሕዝቶ ዘለዎም መወዓዊዒታት iv. ሰብኣዊ ዝኮነ ነገር ከም ግኡዛዊ መንነት ዘለዎ ጌርካ ምቅራብ ንኣብነት ናይ ሁሉተራ ሰራሕተኛታትን ተወከልትን፡፡ ብገምጋም ትካልና ጎዳእቲ፣ ዓመፅቲ፣ ዝተሰሓሓቱ፣ ኣስቀየምቲ ወይ ውን ናይ ሳልሳይ ኣካል መሰል ቅዳሕን ምልክት ንግድን ዝፃብኡ ተባሂሎም ዝተገምገሙን ምስቲ ናይ ኣጠቃቅማ ውዕልን ዘይስማዕምዑ ትሕዝቶታት ትካልና ካብዚ ገፅ ናይ ምልዓል ሙሉእ መሰል ይህልዎ፡፡ ከምዚኦም ዝበሉ ትሕዝቶታት ቀልጢፎም ካብዚ ገፅ ዘይምልዓሎም ወይ ናይ ምድንጓይ ኩነታት ምስዝህሉ ትካልና ሓላፍነት ኣይወስድን፡፡ ክነልዕሎም እንክእል ዓይነት ትሕዝቶ ዘለዎም ነገራት እንተድኣለጦፎም በቲ ዝአተውዎ ናይ ኣጠቃቅማ ውዕሊ፣ ኣብ ተግባር ዝወዓለ ሕጊ፣ ስርዓተ ቁፅፅርን ቅቡላት ሓፈሻዊ ናይ ኦንላየን ኣሰራርሓን መሰረት ፍቁድ ምኳኑ ተረዲኦም ንክነልዕሎም ሙሉእ ፍቃዶም ክኮን ይግባእ፡፡ ትካል በቲ ትጥቅዕዎ ነገር ትሕዝቶ ምክንያት ንዓኩም ኮነ ካልእ ሳልሳይ ተጠቃሚ ወገን ብዝኮነ መንገዲ ተሓተትቲ ናይ ምግባር ዓላማ የብሉን፡፡ ይኩን ምበር ዝኮነ ይኩን በቲ ዝተዘርገሐ ክፍቲ ናይ መራከቢ መንገዲ ኣቢሎም ዝጥቅዕዎምን ናይ ውዕሊ፣ ናይ ሳልሳይ ወገን ናይ ቅዳሕን ንግዲ ምልትን ጥሕሰት ዘይብሎምን ንብረታት ኩሎም ናይ ሁሉተራ ሃፍትታት ምኳኖም ብምቅባል ኣብ ልዕሊ ዞም ምህርትታት ቀፃሊ፣ ዘይቋረፅ፣ ዓለም ለካዊ፣ ውልቃዊ፣ በይናዊ ሙሉእ ንግዲ ፍቃድ ምብዛሕ፣ ምምሕያሽ፣ ምምዕርራይ፣ ምሽግጋር፣ ምሕታም፣ ንህዝቢ ክፉት ናይ ምግባር ወይ ውን ምዝርጋሕ ናይ ምስራሕ መሰል ይህልዎ፡፡ እዚ ከምቲ ቅድም ክብል ዝተገልፀ ነቶም ብክፉት ናይ ርክብ መንገዲ ናብ ገፅና ኣታዊ ዝኮኑ ንብረታት ጥራሕ ይምልከት ፣ ኣብ ልዕሊኦም ውን ተግባራዊ ይከውን፡፡ እዚ ነቶም ኣብዚ ገፅ ዝርከቡ ንብረታት ኣመልኪቶም ዝገብርዎም መደበኛ ናይ ምዝገባ ከይዲ ኣቢሎም ንዝህቡና ሓበሬታ ዘካትትን ዝምከትን ኣይኮነን፡፡ ሁሉተራን ኣሓት ትካላቱን ካልኦት ዝተፀንበሩ ኣካላት ከምኒ ዳይሬክተራት፣ ኦፊሰራት፣ ስራሕ መካየድቲ ፣ ሰራሕተኛታት፣ ገበርቲ ሰናይ፣ ውክልና ዝተውሃቦም ትካላት፣ ውሃብቲ ንግዲ ፍቃድ ዝኮነ ይኩን ጉድኣት ከይበፅሖም ካብ ኪሳራ፣ ወፃኢታት፣ ጉድኣት ዘስዕቦ ክፍሊት ዋጋ ንሕጋዊን ምክንያታውን ዝኮኑ ካብ ናይ ኣጠቃቅማ ውዕሊን ካልኦት ተዛመድቲ ምስ ብስሞም ዝተከፈተ ኣካውንት ዝተኣሳሰሩ ግዴታታት ጥሕሰት ዝመፅእ ክፍሊት ናይ ምክፋል ግዴታ ኣለዎም፡፡ በዚ ምክንያት ናብ ትካልና ዝመፅአና ናይ ክፍሊት ሕቶ ብቀጥታ ናብ ተጠቃሚ ዝሰጋገር ምኳኑ ምርዳእ የድሊ፡፡ ኣብ ከምዚ ዝበለ ዓይነት ኩነታት ምስ ዝእቶ ምክንያታዊ ዝኮነ ካብ ትካልና ዝቀርብ ሕቶ ግቡእ ምላሽ ንምሃብ ምትሕብባር ክገብሩ ይግባእ፡፡</p>
    <p class="headline">ውሱንነት መሰል ውሕስና</p>
    <p class="content" style="padding-left:20px;"><strong>(፩)</strong> ድሕረ ገፅና ኣብ ዝጥቀሙሉ እዋን ኩሎም እነቅርቦም ምህርትታት "ከምዘለውዎ"ን "እቶም ዝረከቡ"ን ጥራሕ ምኳኖም ክርድኡን ክስማዕምዑን ይግባእ፡፡ እዚ ማለት ውን እዞም ዝስዕቡ ውሕስናታት ምረግጋፅ ከምዘይከኣል ንምግላፅ ዩ፡፡</p>
